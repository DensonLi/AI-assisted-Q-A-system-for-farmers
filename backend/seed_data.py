async def main():
    async with engine.begin() as conn:
        print("✅ 数据库连接成功")

        # ── 1. 写入省级 ───────────────────────────────────────────────────────
        print("⏳ 写入省级区域...")
        for code, name, full_name, agro_zone in PROVINCES:
            await conn.execute(text("""
                INSERT INTO regions (code, name, full_name, level, parent_id, agro_zone)
                VALUES (:code, :name, :full_name, 1, NULL, :agro_zone)
                ON CONFLICT (code) DO NOTHING
            """), {"code": code, "name": name, "full_name": full_name, "agro_zone": agro_zone})

        # 建立 code→id 映射（省）
        rows = await conn.execute(text("SELECT id, code FROM regions WHERE level=1"))
        province_id_by_code = {row[1]: row[0] for row in rows}
        print(f"  省级区域：{len(province_id_by_code)} 条")

        # ── 2. 写入市级 ───────────────────────────────────────────────────────
        print("⏳ 写入市级区域...")
        for code, name, full_name, prov_code in CITIES:
            parent_id = province_id_by_code.get(prov_code)
            if not parent_id:
                print(f"  [警告] 找不到省 {prov_code}，跳过城市 {name}")
                continue
            await conn.execute(text("""
                INSERT INTO regions (code, name, full_name, level, parent_id, agro_zone)
                VALUES (:code, :name, :full_name, 2, :parent_id,
                    (SELECT agro_zone FROM regions WHERE id=:parent_id))
                ON CONFLICT (code) DO NOTHING
            """), {"code": code, "name": name, "full_name": full_name, "parent_id": parent_id})

        rows = await conn.execute(text("SELECT id, code FROM regions WHERE level=2"))
        city_id_by_code = {row[1]: row[0] for row in rows}
        print(f"  市级区域：{len(city_id_by_code)} 条")

        # ── 3. 写入县级 ───────────────────────────────────────────────────────
        print("⏳ 写入县级区域...")
        for code, name, full_name, city_code in COUNTIES:
            parent_id = city_id_by_code.get(city_code)
            if not parent_id:
                print(f"  [警告] 找不到市 {city_code}，跳过县 {name}")
                continue
            await conn.execute(text("""
                INSERT INTO regions (code, name, full_name, level, parent_id, agro_zone)
                VALUES (:code, :name, :full_name, 3, :parent_id,
                    (SELECT agro_zone FROM regions WHERE id=:parent_id))
                ON CONFLICT (code) DO NOTHING
            """), {"code": code, "name": name, "full_name": full_name, "parent_id": parent_id})

        rows = await conn.execute(text("SELECT COUNT(*) FROM regions WHERE level=3"))
        cnt = rows.scalar()
        print(f"  县级区域：{cnt} 条")

        # ── 4. 写入作物 ───────────────────────────────────────────────────────
        print("⏳ 写入作物目录...")
        for code, name, category, description, aliases_json in CROPS:
            await conn.execute(text("""
                INSERT INTO crops (code, name, category, description, aliases)
                VALUES (:code, :name, :category, :description, CAST(:aliases AS jsonb))
                ON CONFLICT (code) DO NOTHING
            """), {
                "code": code, "name": name, "category": category,
                "description": description, "aliases": aliases_json,
            })

        rows = await conn.execute(text("SELECT id, code FROM crops"))
        crop_id_by_code = {row[1]: row[0] for row in rows}
        print(f"  作物：{len(crop_id_by_code)} 条")

        # ── 5. 写入物候期 ─────────────────────────────────────────────────────
        print("⏳ 写入物候期...")
        inserted = 0
        for crop_code, zone_key, stage_name, sm, sd, em, ed, description, activities_json in PHENOLOGY:
            crop_id = crop_id_by_code.get(crop_code)
            if not crop_id:
                print(f"  [警告] 找不到作物 {crop_code}")
                continue
            agro_zone = Z.get(zone_key, zone_key)
            result = await conn.execute(text("""
                INSERT INTO phenology_stages
                    (crop_id, agro_zone, stage_name, start_month, start_day,
                     end_month, end_day, description, key_activities)
                VALUES
                    (:crop_id, :agro_zone, :stage_name, :sm, :sd,
                     :em, :ed, :description, CAST(:activities AS jsonb))
                ON CONFLICT (crop_id, agro_zone, stage_name) DO NOTHING
            """), {
                "crop_id": crop_id, "agro_zone": agro_zone, "stage_name": stage_name,
                "sm": sm, "sd": sd, "em": em, "ed": ed,
                "description": description, "activities": activities_json,
            })
            inserted += result.rowcount
        print(f"  物候期：{inserted} 条新增")

        total_regions = (await conn.execute(text("SELECT COUNT(*) FROM regions"))).scalar()
        total_crops   = (await conn.execute(text("SELECT COUNT(*) FROM crops"))).scalar()
        total_pheno   = (await conn.execute(text("SELECT COUNT(*) FROM phenology_stages"))).scalar()

    print("\n🎉 种子数据写入完成！")
    print(f"   区域总计：{total_regions}")
    print(f"   作物总计：{total_crops}")
    print(f"   物候期总计：{total_pheno}")
    await engine.dispose()


if __name__ == "__main__":
    asyncio.run(main())
